<h5>Herramienta para la evaluación de la calidad de los registros administrativos (HECRA)</h5>

<p>La HECRA es un instrumento de <strong>autodiagnóstico y autoevaluación de la calidad de los registros administrativos</strong> que son susceptibles de ser aprovechados para la generación de información estadística y geográfica, no solo de los utilizados para fines de evaluación de instrumentos de planeación, sino también aquellos derivados del quehacer gubernamental. Es importante señalar que el uso de la HECRA no significa que el IPLANEG otorgue el aval de calidad a algún registro administrativo, debido a que únicamente es un instrumento de autodiagnóstico diseñado para identificar las áreas de oportunidad y elaborar un plan de acción con el fin de mejorar la calidad de sus registros.</p>

<p>Objetivos:</p>

<ul>
    <li>Evaluar y mejorar la calidad de los Registros Administrativos a ser usados con fines estadísticos.</li>
    <li>Detectar problemas y oportunidades de mejora de los registros analizados.</li>
    <li>Identificar y analizar las causas de los problemas.</li>
    <li>Implementar planes de acciones correctivas y preventivas.</li>
</ul>

